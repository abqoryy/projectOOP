    private void daftarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_daftarButtonActionPerformed

        // masukin data ke database
        try{
            
            String sql = "INSERT INTO `pasien_puskesmas`(`nikPasien`, `namaPasien`, `umurPasien`, `alamatPasien`, `jenisKelamin`, `noTelp`, `hari`, `tanggal`, `waktu`,`bulan`, `poli`, `keluhan`) "
                    + "VALUES ('"+nikTextBox.getText()+"','"+namaTextBox.getText()+"','"+umurTextBox.getText()+"','"+alamatTextBox.getText()+"','"+JenisKelamincb.getSelectedItem()+"','"+noTelpTextBox.getText()+"',"
                    + "'"+hariComboBox.getSelectedItem()+"','"+tanggalComboBox.getSelectedItem()+"','"+waktuComboBox.getSelectedItem()+"','"+bulanComboBox.getSelectedItem()+"','"+poliComboBox.getSelectedItem()+"','"+keluhanTextBox.getText()+"')";
            
            java.sql.Connection conn = (Connection)Konfig.configDB();
            java.sql.PreparedStatement state = conn.prepareStatement(sql);
            state.execute();
            JOptionPane.showMessageDialog(null, "Berhasil Mendaftar");
            tampilkan_data();
            kosongkan_form();
            
        
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
            
        }
            
       
    }//GEN-LAST:event_daftarButtonActionPerformed

