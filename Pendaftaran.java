    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        
        int delete = tabelPasien.getSelectedRow();
        
        tabelPasien.getValueAt(delete, 1);
        
        int question = JOptionPane.showConfirmDialog(null, "Yakin Ingin Menghapus Data?","Konfirmasi", JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(question == JOptionPane.OK_OPTION){
            try{
                Connection conn = Konfig.configDB();
                String sql = "DELETE FROM `pasien_puskesmas` WHERE nikPasien = '"+nikTextBox.getText()+"'";
                PreparedStatement deleteData = conn.prepareStatement(sql);
                
                deleteData.execute();
                
                JOptionPane.showMessageDialog(null, "Data Terhapus");
            }catch(SQLException e){
                System.out.println("Terjadi Kesalahan");
            }
            tampilkan_data();
            kosongkan_form();
            
            
            if(question == JOptionPane.CANCEL_OPTION){
                
            }
        }
    }//GEN-LAST:event_hapusButtonActionPerformed
    
    
    
