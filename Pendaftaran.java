    private void tampilkan_data(){
        DefaultTableModel tabel_pasien = new DefaultTableModel();
        tabel_pasien.addColumn("No");
        tabel_pasien.addColumn("NIK");
        tabel_pasien.addColumn("Nama");
        tabel_pasien.addColumn("Umur");
        tabel_pasien.addColumn("Alamat");
        tabel_pasien.addColumn("Jenis Kelamin");
        tabel_pasien.addColumn("No Telp");
        tabel_pasien.addColumn("Hari");
        tabel_pasien.addColumn("Tanggal");
        tabel_pasien.addColumn("Bulan");
        tabel_pasien.addColumn("Waktu");
        tabel_pasien.addColumn("Poli");
        tabel_pasien.addColumn("Keluhan");
        
        try{
            int no = 1;
            String sql = "SELECT * FROM pasien_puskesmas";
            Connection conn = Konfig.configDB();
            Statement state = conn.createStatement();
            ResultSet res = state.executeQuery(sql);
            
            while(res.next()){
                tabel_pasien.addRow(new Object[]{no++,res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),
                    res.getString(6),res.getString(7),res.getString(8),res.getString(9),res.getString(10),res.getString(11),res.getString(12)});
            }
            
            tabelPasien.setModel(tabel_pasien);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "gagal");
        }
        
    }
    
